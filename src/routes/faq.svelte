	<h2>¿Dónde puedo sacar el permiso I-94?</h2>
	<p>
		El permiso I-94 en Tijuana se puede sacar en dos lugares: 
		<br /><br />
		a) En la Garita de San Ysidro, <strong>antes</strong> de cruzar hay una pequeña oficina, donde se puede sacar el permiso.
		<br /><br />
		b) En la Garita de Otay, <strong>después</strong> de cruzar hay una pequeña oficina, donde se puede sacar el permiso.
	</p>
