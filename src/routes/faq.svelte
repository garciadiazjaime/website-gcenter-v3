	<h1>Preguntas Frecuentes</h1>
	
	<h2>¿Está cerrada la frontera en Tijuana?</h2>
	<p>
		La frontera seguirá cerrada para viajes no esenciales (principalmente turistas) hasta Octubre 21, 2020.
		<br /><br />
		La fecha se ha ido extendiendo mes tras mes, así que seguramente se seguirá posponiendo.
		<br />
		<br />
		Fuente: 
		<br />
		<a href="https://publish.twitter.com/?query=https%3A%2F%2Ftwitter.com%2FSRE_mx%2Fstatus%2F1306624044016439296&widget=Tweet" target="_blank" rel="nofollow">SRE</a>
	</p>

	<h2>¿Puedo cruzar si mi pasaporte estadounidense está vencido?</h2>
	<p>
		Si eres ciudadano estadounidense y piensas cruzar la frontera necesitas tener tu pasaporte vigente.
		<br /><br />
		Si estas en Tijuana puedes acudir al consulado. Página del consulado americano en Tijuana:
		<br />
		<a href="https://mx.usembassy.gov/embassy-consulates/tijuana/" target="_blank" rel="nofollow">US Embassy</a>

		<br />
		<br />
		Fuente:
		<br />
		<a href="https://support.peninsulavisa.com/hc/en-us/articles/360001272448-My-US-passport-expired-while-I-m-abroad-Can-I-enter-back-into-the-US-on-an-expired-passport-" target="_blank" rel="nofollow">Peninsula Visa</a>
	</p>

	<h2>¿A qué hora cierra la garita de San Ysidro / Otay?</h2>
	<p>
		Tanto para carros como personas, la garita está abierta las 24hrs.
		<br />
		<br />
		Sin embargo para cruce comercial (principalmente tractores) solo de 6am a 8pm.
		<br />
		<br />
		Fuente:
		<br />
		<a href="https://www.cbp.gov/" target="_blank" rel="nofollow">CBP</a>
	</p>

	<h2>¿Cuánto tiempo hay de espera en la garita de San Ysidro / Otay?</h2>
	<p>
		La información del tiempo de espera en garitas la puedes encontrar en nuestra página principal:
		<br />
		<br />
		<a href="https://www.garitacenter.com/">Garita Center</a>
	</p>

	<h2>¿Es cierto que no hay paso de California a Tijuana?</h2>
	<p>
		La recomendación por parte de las autoridades americanas es de evitar cruzar a México. 
		<br /><br />
		Sin embargo la frontera para cruzar a México se encuentra abierta. Así que sí se puede cruzar de California a Tijuana.
		<br /><br />
		Solo recuerda que para el regreso necesitas tener tu pasaporte vigente.

		<br />
		<br />
