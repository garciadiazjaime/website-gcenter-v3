	<h2>¿Es cierto que no hay paso de California a Tijuana?</h2>
	<p>
		La recomendación por parte de las autoridades americanas es de evitar cruzar a México. 
		<br /><br />
		Sin embargo la frontera para cruzar a México se encuentra abierta. Así que sí se puede cruzar de California a Tijuana.
		<br /><br />
		Solo recuerda que para el regreso necesitas tener tu pasaporte vigente.

