	<h1>Preguntas Frecuentes</h1>
	
	<h2>¿Están cerradas las Garitas de Tijuana?</h2>
	<p>
		Las Garitas de Tijuana seguirán cerradas para viajes no esenciales (principalmente turistas) hasta Febrero 21, 2021.
		<br /><br />
		La fecha se ha ido extendiendo mes tras mes, así que seguramente se seguirá posponiendo.
		<br />
		<br />
		Fuente: 
		<br />
		<a href="https://twitter.com/dhs_wolf/status/1337427031735345153?s=21" target="_blank" rel="nofollow">DHS</a>
	</p>

	<h2>¿Necesito prueba de Covid para cruzar a Estados Unidos?</h2>
	<p>
		La prueba de Covid se pide para aquellas personas que entrán a Estados Unidos por Aire, aquellos que cruzan por tierra,
		a través de las diferentes garitas que se encuentran en la frontera, ya sea por carro o a pie, de momento no es requisito mostrar prueba de Covid.
		<br />
		<br />
		Fuente:
		<br />
		<a href="https://www.cdc.gov/coronavirus/2019-ncov/travelers/testing-international-air-travelers.html" target="_blank" rel="nofollow">CDC</a>
	</p>

	<h2>¿Como esta la linea?</h2>
	<p>
		Es difícil saber con precisión, pero a manera general se pueden mencionar los siguientes tiempos:
	</p>
	<h3>En Carro.</h3>
	<ul>
		<li>Con Sentry por lo menos 30 minutos.</li>
		<li>Sin Sentry por lo menos 1 hora.</li>
	</ul>
	<h3>Caminado</h3>
	<ul>
		<li>Con sentry por lo menos 10 minutos.</li>
		<li>Sin sentry por lo menos 30 minutos.</li>
	</ul>

	<h2>¿Puedo cruzar si mi pasaporte estadounidense está vencido?</h2>
	<p>
		Si eres ciudadano estadounidense y piensas cruzar la frontera necesitas tener tu pasaporte vigente.
		<br /><br />
		Si estas en Tijuana puedes acudir al consulado. Página del consulado americano en Tijuana:
		<br />
		<a href="https://mx.usembassy.gov/embassy-consulates/tijuana/" target="_blank" rel="nofollow">US Embassy</a>

		<br />
		<br />
