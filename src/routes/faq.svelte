	<h2>¿Están abiertas las Garitas de Tijuana?</h2>
	<p>
		Las Garitas de Tijuana abieron Noviembre 8, 2021.
		<br /><br />
		Es necesario contar con certificado de vacunación COVID. Como siempre es a discreción del oficial el pedirlo.
		<br /><br />
		Fuente: 
		<br />
		<a href="https://www.cbp.gov/newsroom/local-media-release/san-diego-cbp-officials-discuss-upcoming-changes-travel-restrictions" target="_blank" rel="nofollow">CBP</a>
		<br /><br />
		Aquellos vacunados en México, podrán sacar su certificado de vacuncación en la página oficial de la <a href="https://cvcovid.salud.gob.mx/">Secretaría de Salud</a>.
	</p>

	<h2>¿Necesito prueba de Covid para cruzar a Estados Unidos?</h2>
	<p>
		La prueba de Covid ya no es necesaria.
		<br /><br />
		Fuente:
		<br />
		<a href="https://www.cbp.gov/newsroom/coronavirus" target="_blank" rel="nofollow">CBP</a>
